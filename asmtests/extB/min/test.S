# use s0(r8)  - s1(r9)
# use s2(r18) - s11(r27)

# rd = min(rs1,rs2)
#      s2 s3  s4
#      18 19  20
# MIN rd rs1 rs2
.macro test_MIN imm1 imm2
    WRITE_LOG_N str1
    WRITE_S sep
    li s2, 0
    li s3, \imm1
    li s4, \imm2
    MIN 18 19 20
    WRITE_RAW s3
    WRITE_S op
    WRITE_RAW s4
    WRITE_S gives
    WRITE_RAW s2
    WRITE_NL
.endm

